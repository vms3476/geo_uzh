function [cmap] = ndvimap(maplength);
% ColEdit function [cmap] = vegetation2(maplength);
%
% colormap m-file written by ColEdit
% version 1.1 on 17-Jul-2008
%
% input  :	[maplength]	[64]	- colormap length
%
% output :	cmap			- colormap RGB-value array
 
% set red points
r = [ [];...
    [0 0.42553];...
    [0.1641 0.40426];...
    [0.26923 0.29787];...
    [0.375 0];...
    [0.625 0.053191];...
    [0.875 0.031915];...
    [1 0.031915];...
    [] ];
 
% set green points
g = [ [];...
    [0 0.35106];...
    [0.125 0.34043];...
    [0.375 0.89362];...
    [0.625 0.71277];...
    [0.875 0.38298];...
    [1 0.15957];...
    [] ];
 
% set blue points
b = [ [];...
    [0 0.35106];...
    [0.125 0.10638];...
    [0.375 0.074468];...
    [0.625 0];...
    [1 0];...
    [] ];
% ColEditInfoEnd
 
% get colormap length
if nargin==1 
  if length(maplength)==1
    if maplength<1
      maplength = 64;
    elseif maplength>256
      maplength = 256;
    elseif isinf(maplength)
      maplength = 64;
    elseif isnan(maplength)
      maplength = 64;
    end
  end
else
  maplength = 64;
end
 
% interpolate colormap
np = linspace(0,1,maplength);
rr = interp1(r(:,1),r(:,2),np,'linear');
gg = interp1(g(:,1),g(:,2),np,'linear');
bb = interp1(b(:,1),b(:,2),np,'linear');
 
% compose colormap
cmap = [rr(:),gg(:),bb(:)];
